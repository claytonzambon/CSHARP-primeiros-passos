using System;

namespace _46._2_Estrutura_repetitiva_para {
    class Program {
        static void Main(string[] args) {

            for (int i = 0; i < 5; i++) {
                Console.WriteLine("Valor de i: " + i);
            }

            Console.WriteLine("\n\n");

            for (int i = 4; i >= 0; i--) {
                Console.WriteLine("Valor de i: " + i);
            }
        }
    }
}
